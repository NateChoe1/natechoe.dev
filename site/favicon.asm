ico_header:
  dw 0                         ; Reserved
  dw 1                         ; This is a .ico image
  dw 1                         ; No. of images in this file

ico_image_header:
  db 32                        ; Width
  db 32                        ; Height
  db 2                         ; No. of colors
  db 0                         ; Reserved
  dw 1                         ; Color planes
  dw 1                         ; Bits per pixel
  dd bmp_end - bmp_header      ; Image size
  dd bmp_header - ico_header   ; Image offset

